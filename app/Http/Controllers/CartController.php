    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $product = Product::findOrFail($request->input('product_id'));
        Cart::add(['id' => $product->id, 'name' => $product->title, 'qty' => $request->input('quantity'), 'price' => $product->price, 'weight' => 0, 'options' => ['size' => $request->input('size'), 'image' => $product->front_shirt, 'color' => $request->input('color'),'shopname' => $product->shopname]]);
        return redirect()->route('product.show', $product->id)->with('message', 'Successfully added to cart.');
    }

